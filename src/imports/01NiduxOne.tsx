function useScrollAnimations() {
  useEffect(() => {
    // 1. Reveal sections + inner elements
    const revealTargets = document.querySelectorAll("[data-reveal], [data-fade]");
    const revealObserver = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add("revealed");
            revealObserver.unobserve(entry.target);
          }
        });
      },
      { threshold: 0.08, rootMargin: "0px 0px -40px 0px" }
    );
    revealTargets.forEach((el) => revealObserver.observe(el));

    // 2. Parallax on scroll
    const parallaxEls = document.querySelectorAll("[data-parallax]");
    function handleParallax() {
      const scrollY = window.scrollY;
      parallaxEls.forEach((el) => {
        const speed = parseFloat((el as HTMLElement).dataset.parallax || "0.1");
        const rect = el.getBoundingClientRect();
        const offset = (rect.top + scrollY - window.innerHeight / 2) * speed;
        (el as HTMLElement).style.transform = `translateY(${offset}px)`;
      });
    }

    // 3. Counter animation for numbers
    const counterEls = document.querySelectorAll("[data-counter]");
    const counterObserver = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            const el = entry.target as HTMLElement;
            const target = parseInt(el.dataset.counter || "0");
            const suffix = el.dataset.counterSuffix || "";
            let current = 0;
            const duration = 2000;
            const step = target / (duration / 16);
            const animate = () => {
              current = Math.min(current + step, target);
              el.textContent = Math.floor(current).toLocaleString() + suffix;
              if (current < target) requestAnimationFrame(animate);
            };
            animate();
            counterObserver.unobserve(el);
          }
        });
      },
      { threshold: 0.5 }
    );
    counterEls.forEach((el) => counterObserver.observe(el));

    window.addEventListener("scroll", handleParallax, { passive: true });
    handleParallax();

    return () => {
      revealObserver.disconnect();
      counterObserver.disconnect();
      window.removeEventListener("scroll", handleParallax);
    };
  }, []);
}

function useHeaderVisibility() {
  const [showHeader, setShowHeader] = useState(false);
  useEffect(() => {
    const handleScroll = () => {
      // check both window and snap container
      const container = document.querySelector("[data-snap-container]");
      const scrollTop = container ? container.scrollTop : window.scrollY;
      setShowHeader(scrollTop > window.innerHeight * 0.8);
    };
    window.addEventListener("scroll", handleScroll, { passive: true });
    const container = document.querySelector("[data-snap-container]");
    container?.addEventListener("scroll", handleScroll, { passive: true });
    handleScroll();
    return () => {
      window.removeEventListener("scroll", handleScroll);
      container?.removeEventListener("scroll", handleScroll);
    };
  }, []);
  return showHeader;
}

